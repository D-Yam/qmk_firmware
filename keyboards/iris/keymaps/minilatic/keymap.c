bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;

    case LFUNC:
    	if(record->event.pressed){
    		layer_on(_LFUNC);
    		update_tri_layer(_LFUNC, _RFUNC, _ADJUST);
    	}else{
    		layer_off(_LFUNC);
    		update_tri_layer(_LFUNC, _RFUNC, _ADJUST);
    	}
    	break;

    case RFUNC:
    	if(record->event.pressed){
    		layer_on(_RFUNC);
    		update_tri_layer(_LFUNC, _RFUNC, _ADJUST);
    	}else{
    		layer_off(_RFUNC);
    		update_tri_layer(_LFUNC, _RFUNC, _ADJUST);
    	}
    	break;

    case ADJUST:
      if (record->event.pressed) {
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
      }
      return false;
      break;
  }
  return true;
}
